
$(document).ready(function(){
    // Detecta el cambio en el elemento <select>
    $("#front_work").change(function(){
        // Obtiene la descripción seleccionada
        var selectedDescription = $("#front_work option:selected").text();
        
        // Asigna la descripción al campo de entrada
        $("#description_work").val(selectedDescription);
    });
});


